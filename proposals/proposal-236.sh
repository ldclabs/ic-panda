    record {
        title = \"Execute commit_proposed_batch() to release ic_message_frontend v2.6.9\";
        url = \"https://dMsg.net/\";
        summary = \"This proposal executes commit_proposed_batch() on 2fvu6-tqaaa-aaaap-akksa-cai to release ic_message_frontend v2.6.9.\n\n1. fix: fix UX issues.\";
